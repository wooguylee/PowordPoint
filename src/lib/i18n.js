export const translate = (language, key, vars = {}) => {
  const value = key.split('.').reduce((acc, part) => acc?.[part], messages[language] || messages.en)
  const resolved = typeof value === 'string' ? value : key
  return Object.entries(vars).reduce(
    (text, [name, replacement]) => text.replaceAll(`{${name}}`, `${replacement}`),
    resolved,
  )
}

export const formatDateTime = (language, value) => {
  try {
    return new Intl.DateTimeFormat(language === 'ko' ? 'ko-KR' : 'en-US', {
      year: 'numeric',
      month: '2-digit',
      day: '2-digit',
      hour: '2-digit',
      minute: '2-digit',
    }).format(new Date(value))
  } catch {
    return `${value || ''}`
  }
}
